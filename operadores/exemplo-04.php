<?php  

$a = 30;
$b = 50;

// comparando se A é maior que B
var_dump($a > $b);
echo "<br>";

// comparando se A é menor que B
var_dump($a < $b);
echo "<br>";

// comparando se A é igual a B, ou seja, verifica somente o valor
var_dump($a == $b);
echo "<br>";

// comparando se A é identico a B, ou seja se o tipo é o mesmo
var_dump($a === $b);
echo "<br>";

// comparando se A é diferente de B
var_dump($a != $b);
echo "<br>";

// comparando se A é completamente diferente de B
var_dump($a !== $b);
echo "<br>";

?>